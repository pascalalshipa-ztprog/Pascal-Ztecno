    <h2>I. Pendahuluan</h2>
    <p>
        Perjalanan panjang sejarah Indonesia untuk berdiri sebagai entitas negara yang berdaulat dipenuhi oleh pengorbanan kolektif berbagai elemen bangsa, di mana para pemuda dan mahasiswa senantiasa mengambil posisi di garda terdepan perjuangan fisik[cite: 19]. Namun, memasuki abad ke-21, hakikat ancaman terhadap kedaulatan negara telah mengalami transformasi radikal[cite: 16, 20]. Apabila pada masa lalu bela negara diidentikkan secara eksklusif dengan mobilisasi fisik dan perjuangan senjata melawan kolonialisme (pertahanan tradisional)[cite: 16, 18, 19], maka era kontemporer menyajikan spektrum ancaman yang bersifat multidimensional dan nirmiliter[cite: 19, 20]. Globalisasi, pesatnya perkembangan teknologi informasi, serta kaburnya batas-batas geografis (<i>borderless society</i>) membawa tantangan sosiokultural yang sangat kompleks bagi persatuan dan kesatuan bangsa Indonesia[cite: 15, 20].
    </p>
    <p>
        Urgensi bela negara nirmiliter saat ini berakar pada maraknya ancaman non-fisik yang langsung menyasar jantung ketahanan nasional, yakni ideologi, sosial, budaya, dan psikologis masyarakat[cite: 15, 16]. Fenomena abad ke-21 yang ditandai dengan penetrasi digital secara masif telah melahirkan era <i>post-truth</i>, sebuah kondisi di mana kebenaran objektif kalah pengaruh oleh emosi pribadi dan keyakinan subjektif dalam membentuk opini publik. Di Indonesia, dinamika digital ini mengejawantah dalam bentuk penyebaran berita bohong (hoaks), ujaran kebencian (<i>hate speech</i>), polarisasi sosial politik, hingga infiltrasi radikalisme transnasional yang merusak kohesi sosial dan nilai luhur persatuan[cite: 15, 16]. Dalam konteks ini, pertahanan negara tidak lagi menjadi monopoli eksklusif militer, melainkan menjadi tanggung jawab mutlak setiap warga negara sebagaimana diamanatkan secara konstitusional dalam Pasal 27 Ayat (3) dan Pasal 30 Ayat (1) Undang-Undang Dasar Negara Republik Indonesia Tahun 1945[cite: 16, 19].
    </p>
    <p>
        Mahasiswa, sebagai entitas intelektual kelas menengah, menempati posisi strategis yang unik dalam struktur sosial kemasyarakatan[cite: 16, 18, 19]. Mahasiswa tidak hanya berfungsi sebagai peserta didik di jenjang pendidikan tinggi, melainkan mengemban tanggung jawab moral, sosial, dan akademik sebagai <i>agent of change</i> (agen perubahan) serta <i>social control</i> (kontrol sosial)[cite: 15, 17, 18]. Posisi istimewa ini memungkinkan mahasiswa menjadi jembatan penghubung antara gagasan akademis-konseptual dengan realitas empiris di masyarakat[cite: 15, 19]. Oleh karena itu, keterlibatan aktif mahasiswa dalam mengonseptualisasikan dan mempraktikkan bela negara nirmiliter menjadi instrumen penting guna menangkal berbagai potensi disintegrasi bangsa[cite: 15, 16].
    </p>
    <p>
        Berdasarkan latar belakang tersebut, rumusan masalah dalam penelitian ini adalah: (1) Bagaimana peran mahasiswa dalam menjaga persatuan bangsa Indonesia di tengah gempuran tantangan era digital dan <i>post-truth</i>?[cite: 15] (2) Bagaimana bentuk reaktualisasi kontribusi mahasiswa dalam upaya bela negara nirmiliter di era kontemporer?[cite: 15, 16] Adapun tujuan penelitian ini adalah untuk menganalisis secara mendalam peran strategis mahasiswa dalam menjaga persatuan bangsa, serta mendeskripsikan secara komprehensif bentuk-bentuk implementasi bela negara nirmiliter yang relevan bagi kalangan mahasiswa dalam lingkup akademik maupun kemasyarakatan[cite: 15].
    </p>

    <h2>II. Metode Penelitian</h2>
    <p>
        Penelitian ini menggunakan pendekatan kualitatif deskriptif dengan metode studi literatur (<i>literature review</i>)[cite: 15, 16]. Pendekatan kualitatif dipilih agar peneliti dapat mengeksplorasi, memahami, dan memaparkan konsep-konsep abstrak terkait bela negara, persatuan, serta peran sosial mahasiswa secara holistik dan mendalam[cite: 15, 16]. Studi literatur dilaksanakan melalui penelusuran sistematis, penelaahan kritis, dan sintesis terhadap berbagai dokumen akademis dan produk hukum yang relevan dengan topik kajian[cite: 15, 16, 20].
    </p>
    <p>
        Sumber data penelitian ini diklasifikasikan ke dalam dua kategori utama: dokumen hukum resmi dan literatur ilmiah akademik[cite: 15, 20]. Dokumen hukum yang dianalisis mencakup Undang-Undang Dasar Negara Republik Indonesia Tahun 1945, Undang-Undang Nomor 3 Tahun 2002 tentang Pertahanan Negara, dan Undang-Undang Nomor 23 Tahun 2019 tentang Pengelolaan Sumber Daya Nasional untuk Pertahanan Negara[cite: 15, 16]. Adapun literatur ilmiah berupa jurnal ilmiah nasional bereputasi terindeks (seperti Google Scholar dan SINTA) serta buku referensi di bidang Pendidikan Kewarganegaraan dan ketahanan nasional yang diterbitkan dalam kurun waktu sepuluh tahun terakhir (2016-2026)[cite: 15].
    </p>
    <p>
        Kriteria inklusi dalam pemilihan literatur yang dianalisis meliputi: (a) artikel jurnal atau buku yang mengkaji peran mahasiswa dalam bela negara; (b) publikasi yang membahas ketahanan nasional nirmiliter di era digital atau globalisasi; serta (c) literatur yang berfokus pada implementasi Pancasila sebagai instrumen integrasi bangsa[cite: 15, 16]. Analisis data dilakukan dengan teknik analisis isi (<i>content analysis</i>) dan analisis tematis, di mana data dari berbagai sumber dikumpulkan, dikelompokkan berdasarkan tema bahasan, divalidasi silang (triangulasi sumber data literatur), dan ditarik kesimpulan substantif yang logis dan objektif[cite: 15, 17, 20].
    </p>

    <h2>III. Hasil dan Pembahasan</h2>

    <h3>A. Reaktualisasi Bela Negara di Era Kontemporer: Dari Fisik ke Nirmiliter</h3>
    <p>
        Konsep bela negara pada era kontemporer menuntut adanya reaktualisasi makna yang tidak lagi terbatas pada pertahanan fisik atau angkat senjata demi menghadapi agresi militer asing[cite: 16, 18, 19]. Berdasarkan Undang-Undang Nomor 23 Tahun 2019 tentang Pengelolaan Sumber Daya Nasional untuk Pertahanan Negara, pengabdian bela negara dapat dilakukan secara nirmiliter melalui profesi dan keahlian masing-masing warga negara[cite: 15, 16]. Konsep pertahanan nirmiliter menekankan pada penguatan aspek non-fisik, seperti ketahanan ideologi, kedaulatan informasi, kemandirian ekonomi, dan kelestarian sosial budaya[cite: 15, 16, 19]. Bagi mahasiswa, reaktualisasi ini berarti memindahkan medan pertempuran dari wilayah fisik ke wilayah intelektual, inovasi, dan literasi[cite: 15, 16, 18].
