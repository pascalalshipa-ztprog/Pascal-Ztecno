        <div class="abstract-title">Abstract</div>
        <p class="no-indent" style="text-align: justify; margin-bottom: 10px;">
            <i>State defense awareness is a crucial foundation for the survival of the Unitary State of the Republic of Indonesia (NKRI) amidst the dynamic global challenges of the 21st century, which have shifted from physical-military to non-military threats[cite: 16]. This research aims to critically analyze the strategic role of students as agents of change and moral forces in maintaining national unity in the digital era and the post-truth phenomenon[cite: 15]. Utilizing a descriptive qualitative method based on a literature review, this study explores the contemporary reactualization of state defense through the utilization of specific scientific disciplines and the enforcement of professional ethics[cite: 15, 16]. The results of the analysis indicate that current integration challenges are dominated by the spread of disinformation (hoaxes), hate speech, and social polarization, which have the potential to trigger national disintegration[cite: 15]. Students contribute significantly through five pillars of modern state defense: strengthening digital literacy, targeted community service, academic achievement, internalizing Pancasila values, and enhancing collective state defense awareness[cite: 15]. A strong synergy among higher education institutions, student organizations, and the government is the main key to facilitating systematic and sustainable non-military national resilience movements[cite: 15, 16].</i>
        </p>
