        <div class="abstract-title">Abstrak</div>
        <p class="no-indent" style="text-align: justify; margin-bottom: 10px;">
            Kesadaran bela negara merupakan fondasi krusial bagi keberlangsungan Negara Kesatuan Republik Indonesia (NKRI) di tengah dinamika tantangan global abad ke-21 yang bergeser dari ancaman militer-fisik menjadi nirmiliter[cite: 16]. Penelitian ini bertujuan untuk menganalisis secara kritis peran strategis mahasiswa sebagai <i>agent of change</i> dan <i>moral force</i> dalam mempertahankan persatuan dan kesatuan bangsa di era digital dan fenomena <i>post-truth</i>[cite: 15]. Menggunakan metode kualitatif deskriptif berbasis studi literatur (<i>literature review</i>)[cite: 15, 16], penelitian ini mengeksplorasi reaktualisasi bela negara kontemporer melalui pemanfaatan keilmuan spesifik dan penegakan etika profesi[cite: 15, 16]. Hasil analisis menunjukkan bahwa tantangan integrasi nasional saat ini didominasi oleh penyebaran disinformasi (hoaks), ujaran kebencian, dan polarisasi sosial yang berpotensi memicu disintegrasi bangsa[cite: 15]. Mahasiswa berkontribusi nyata melalui lima pilar bela negara modern: penguatan literasi digital, pengabdian masyarakat terarah, pencapaian prestasi akademik, internalisasi nilai-nilai Pancasila, dan peningkatan kesadaran bela negara kolektif[cite: 15]. Sinergi yang kuat antara institusi pendidikan tinggi, organisasi mahasiswa, dan pemerintah menjadi kunci utama dalam memfasilitasi gerakan ketahanan nasional nirmiliter yang sistematis dan berkelanjutan[cite: 15, 16].
        </p>
        <div class="keywords">
            <strong>Kata Kunci:</strong> Mahasiswa, Persatuan Bangsa, Bela Negara Nirmiliter, Era Post-Truth, Studi Literatur[cite: 15, 16, 17].
