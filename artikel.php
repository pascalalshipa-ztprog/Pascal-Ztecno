    <h3>B. Tantangan Persatuan Bangsa di Era Digital dan Fenomena Post-Truth</h3>
    <p>
        Di era informasi digital, disintegrasi sosial menjadi ancaman nirmiliter yang paling nyata[cite: 15, 16, 19]. Keberagaman suku, ras, agama, dan antar-golongan (SARA) yang merupakan kekayaan bangsa Indonesia, di era digital justru kerap dieksploitasi oleh pihak-pihak yang tidak bertanggung jawab[cite: 15, 20]. Era <i>post-truth</i> memperparah kondisi ini dengan memprioritaskan penyebaran konten sensasional yang memicu kemarahan, ketakutan, dan kebencian sosiopolitik daripada menyajikan kebenaran berbasis data objektif[cite: 15]. Dampak langsung dari situasi ini adalah melemahnya rasa saling percaya antar-anak bangsa (<i>social distrust</i>) dan meningkatnya potensi konflik horizontal[cite: 15, 19].
    </p>
    <p>
        Pengaruh globalisasi juga membawa infiltrasi nilai-nilai asing yang bertentangan dengan jati diri bangsa Indonesia[cite: 16, 20]. Masuknya paham ekstremisme, radikalisme, serta sikap individualistis ekstrem yang disebarkan melalui platform digital berpotensi mengikis rasa cinta tanah air dan melemahkan kesetiaan terhadap ideologi Pancasila[cite: 15, 16, 20]. Hal ini sejalan dengan temuan Putri (2021) yang menegaskan bahwa internalisasi nilai-nilai Pancasila menjadi benteng mutlak bagi generasi muda agar tidak terombang-ambing oleh arus globalisasi yang destruktif[cite: 15, 20]. Tanpa adanya filter ideologis yang kuat, persatuan nasional akan terus berada dalam kondisi rentan[cite: 15, 20].
    </p>

    <h3>C. Tiga Pilar Kontribusi Mahasiswa dalam Bela Negara Modern</h3>
    <p>
        Berdasarkan sintesis kajian pustaka, kontribusi mahasiswa dalam bela negara nirmiliter kontemporer dapat dikristalisasikan ke dalam tiga pilar utama sebagai berikut:
    </p>
    <p class="no-indent">
        <b>1. Pilar Intelektual dan Prestasi Akademik (Intelektualisasi Bela Negara)</b><br>
        Mahasiswa membela negara dengan cara meningkatkan kualitas diri, kapasitas berpikir kritis, serta penguasaan ilmu pengetahuan dan teknologi[cite: 15, 17, 18]. Prestasi akademik dan hasil riset yang bereputasi bukan hanya sekadar pemenuhan kewajiban kuliah, melainkan bentuk pembuktian kualitas sumber daya manusia (SDM) Indonesia di kancah internasional[cite: 15, 18]. Melalui inovasi sains, mahasiswa menciptakan solusi nyata atas berbagai problem kemandirian bangsa (misalnya ketahanan pangan, energi, dan kesehatan), yang secara langsung memperkuat ketahanan nasional[cite: 15, 16].
    </p>
    <p class="no-indent">
        <b>2. Pilar Sosial dan Pengabdian kepada Masyarakat (Social Force)</b><br>
        Bela negara harus mewujud dalam aksi nyata yang dirasakan langsung oleh masyarakat kelas bawah[cite: 15, 17, 18]. Melalui program Kuliah Kerja Nyata (KKN) tematik, aksi bakti sosial, pengajaran di daerah terpencil, maupun aksi kerelawanan dalam penanganan bencana, mahasiswa secara langsung memperkokoh solidaritas sosial dan ketahanan masyarakat dari bawah[cite: 15, 16, 17]. Kegiatan sosial kemasyarakatan ini juga berfungsi sebagai sarana sosialisasi dan penularan semangat nasionalisme kepada warga sekitar[cite: 15, 16, 19].
    </p>
    <p class="no-indent">
        <b>3. Pilar Literasi Digital dan Kontraterorisme Siber (Digital Defense)</b><br>
        Mahasiswa memiliki peran sentral sebagai pelopor literasi digital (<i>digital pioneer</i>)[cite: 15, 16, 17]. Sebagai generasi yang fasih teknologi, mahasiswa dituntut secara aktif memproduksi konten-konten kreatif positif bertema kebangsaan, perdamaian, dan kerukunan nusantara guna mengimbangi narasi hoaks dan ujaran kebencian di ruang siber[cite: 15, 16]. Upaya menangkal hoaks dan mengampanyekan persatuan nasional melalui media sosial merupakan tindakan bela negara yang sangat krusial guna meredam polarisasi di era <i>post-truth</i>[cite: 15, 16].
    </p>

    <h3>D. Sinergi Keilmuan Spesifik dan Etika Profesi Mahasiswa</h3>
    <p>
        Bela negara nirmiliter akan menjadi jauh lebih efektif apabila disinergikan dengan latar belakang keilmuan spesifik dan penegakan etika profesi akademis masing-masing mahasiswa[cite: 15, 16]. Mahasiswa dari rumpun ilmu teknologi informasi (Informatika), misalnya, dapat berkontribusi melalui pengembangan sistem pertahanan siber, pembuatan aplikasi deteksi hoaks, atau pelopor keamanan data nasional[cite: 17]. Mahasiswa kedokteran dan kesehatan mengimplementasikan bela negara dengan meningkatkan derajat kesehatan masyarakat di daerah perifer, sementara mahasiswa keguruan (kependidikan) berfokus pada pemberantasan buta aksara dan pengajaran nilai-nilai karakter bangsa bagi generasi penerus di pelosok negeri[cite: 15, 16, 19].
    </p>
    <p>
        Penegakan etika profesi dan integritas akademik juga merupakan bentuk riil bela negara[cite: 16]. Mahasiswa yang menolak plagiarisme, bersikap jujur dalam penelitian ilmiah, menyuarakan kritik sosial sosiopolitik secara santun dan konstruktif, serta mematuhi hukum merupakan wujud nyata pengamalan Pancasila sila kelima dan kesatuan[cite: 16, 18]. Ketika setiap mahasiswa menjalankan profesi akademiknya secara profesional, jujur, dan berorientasi pada kemaslahatan publik, maka fondasi moral bangsa Indonesia akan terjaga dengan sangat kokoh dari ancaman pembusukan internal (korupsi moral)[cite: 15, 16, 18].
    </p>

    <h2>IV. Kesimpulan dan Saran</h2>

    <h3>A. Kesimpulan</h3>
    <p>
        Konsep bela negara pada era kontemporer tidak lagi dipahami secara sempit sebatas latihan fisik militeristis, melainkan telah bergeser ke arah gerakan bela negara nirmiliter yang dinamis, adaptif, dan berbasis pada keahlian serta profesi[cite: 15, 16, 18]. Di tengah derasnya arus globalisasi dan ancaman disintegrasi nasional di era digital serta <i>post-truth</i>, mahasiswa memegang kedudukan sentral dan strategis sebagai <i>agent of change</i> dan <i>moral force</i>[cite: 15, 17, 18]. Mahasiswa mampu mengintegrasikan kapasitas intelektualnya dengan aksi kemasyarakatan nyata guna menjaga keutuhan NKRI[cite: 15, 17, 19]. Dengan mengimplementasikan lima nilai dasar bela negara—cinta tanah air, sadar berbangsa/bernegara, setia pada Pancasila, rela berkorban, dan memiliki kemampuan awal bela negara—secara konsisten, mahasiswa Indonesia siap menjadi pilar utama penjaga keutuhan bangsa[cite: 15, 16, 18].
    </p>

    <h3>B. Saran dan Rekomendasi</h3>
    <p class="no-indent">
        <b>1. Bagi Perguruan Tinggi:</b><br>
        Disarankan agar perguruan tinggi merevitalisasi kurikulum Pendidikan Kewarganegaraan dengan metode pembelajaran yang interaktif dan berbasis penyelesaian kasus nyata (<i>case method</i>)[cite: 16, 18]. Institusi juga harus memperbanyak skema KKN tematik bela negara, mendukung riset-riset mahasiswa yang berorientasi pada ketahanan nasional, serta mengintegrasikan pendidikan karakter berbasis Pancasila ke dalam ekosistem kampus yang inklusif dan toleran[cite: 15, 16, 17].
    </p>
    <p class="no-indent">
        <b>2. Bagi Organisasi Mahasiswa:</b><br>
        Organisasi kemahasiswaan (BEM, UKM, Himpunan Mahasiswa) direkomendasikan untuk beralih dari program kerja konvensional menuju kegiatan kreatif kemasyarakatan yang menyentuh persoalan riil masyarakat[cite: 15, 17, 18]. Organisasi mahasiswa harus menjadi motor penggerak literasi digital, aktif mengedukasi publik dalam menangkal hoaks, serta memupuk kerukunan lintas budaya dan agama dalam setiap aktivitas kepemudaan[cite: 15, 16, 17].
    </p>
    <p class="no-indent">
        <b>3. Bagi Pemerintah:</b><br>
        Pemerintah, melalui kementerian terkait (Kemendikbudristek dan Kementerian Pertahanan), perlu memperluas ruang kolaborasi taktis dengan dunia kampus[cite: 15, 16]. Dukungan berupa program pembinaan kesadaran bela negara yang modern, pendanaan riset kebangsaan, serta penyediaan platform digital edukatif yang resmi sangat diperlukan guna memfasilitasi potensi inovatif mahasiswa demi memperkuat sistem pertahanan nirmiliter Indonesia di kancah global[cite: 15, 16].
    </p>

